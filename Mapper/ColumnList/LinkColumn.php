namespace Manuel\Bundle\UploadDataBundle\Mapper\ColumnList;

use Symfony\Component\OptionsResolver\OptionsResolver;


/**
 * @autor Manuel Aguirre <programador.manuel@gmail.com>
 */
class LinkColumn extends AbstractColumn
{

    public function getType()
    {
        return 'link';
    }

    public function setDefaultOptions(OptionsResolver $resolver)
    {
        parent::setDefaultOptions($resolver); // TODO: Change the autogenerated stub

        $resolver->setDefaults(array(
            'route' => null,
            'url' => null,
            'attr' => array(),
            'parameters' => array(),
            'template' => '@UploadData/Default/column_link.html.twig',
        ));

        $resolver->setDefined(array('condition'));
        $resolver->setAllowedTypes('condition',['Closure', 'callable']);
    }


}
